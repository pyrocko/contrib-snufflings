c
c     subroutine ellcal
c
    
      subroutine ellcal (elat,ax1,ax2,fchi,elmax,elmin,eazi,earea)
      double precision elat,ax1(2),ax2(2),aaxes,baxes,earea,
     +       f1,deg2rad,elkm,fchi,elmax,elmin,eazi,pi
      double precision q2, radloc
      
      pi = 4.d0*datan(1.d0)
      deg2rad = pi/180.d0

      f1 = dcos(deg2rad*elat)

      ax1(2) = ax1(2) * f1
      ax2(2) = ax2(2) * f1

      elkm  = deg2rad*radloc(elat)

      aaxes = elkm*fchi*dsqrt( q2(ax1(1)) + q2(ax1(2)) )
      baxes = elkm*fchi*dsqrt( q2(ax2(1)) + q2(ax2(2)) )

      earea = aaxes*baxes*pi

      if(aaxes.ge.baxes) then

         elmax = aaxes
         elmin = baxes
         eazi = datan2(ax1(2),ax1(1))/deg2rad

      else

         elmax = baxes
         elmin = aaxes
         eazi = datan2(ax2(2),ax2(1))/deg2rad

      endif

      if(eazi.lt.0.d0)   eazi = eazi + 360.d0
      if(eazi.gt.180.d0) eazi = eazi - 180.d0

      return
      end

