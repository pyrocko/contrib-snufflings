      subroutine get_station(file,stat,lat,lon,elev,name,ierr)
C
C         May 21, 1996  Johannes Schweitzer, Bochum
C
C         June 8, 1998  smaller changes JS, NORSAR
C
C         December 3, 1998  CSS-site file included
C
C     Reads the station coordinates and name from the
C     NEIC-station-list file 'stations.dat' 
c
c     or from a CSS30 .site file
C
C     stat  = station-code lower or uper case characters
C             character*5
C
C     lat   = station latitude in degree (real*8)
C
C     lon   = station longitude in degree (real*8)
C
C     elev  = station elevation in meter (real*4)
C
C     name  = station name in full text (character*48)
C
C     ierr  = 0  no problem
C           = 1  station.dat not found
C           = 2  station not found in station list
C           = 3  station has no coordinates
C           = 4  cannot read input format
C
C
C     NEIC station file format:
C
1234  format(A5,1X,i2,i2,f4.1,a1,i3,i2,f4.1,a1,f7.1,a)
C
C     CSS 3.0 Format: 
C         Can only read the first 5 characters (of 6) of site name.
C         Can only read the first 48 characters (of 50) of site 
C         description.
C
1235  format(A5,1x,a1,18x,f9.4,1x,f9.4,1x,f9.4,1x,a48)

      character stat*5,name*48,stat1*5,clat*1,clon*1,statn*5
      character file*80, text*60
      real*8 lat,lon
      real*4 elev,sla,slo
      integer ierr,trimle,idla,imla,idlo,imlo
C
      ierr = 2
      ialt = 0

      do 100 i=1,5
      ii= ichar(stat(i:i))
      if(ii.ge.97 .and. ii.le.122) then
         ii=ii-32
         stat(i:i)=char(ii)
      endif
100   continue

      open (66,file=file(1:trimle(file)))

      read (66,'(a)',err=900,end=900) text
      rewind(66)

      clat = text(15:15)
      clon = text(25:25)
c
c     If format does not fit NEIC format, we assume CSS 3.0 format.
c
      if((clat.ne.'N' .and. clat.ne.'S' .and. clat.ne.' ') .or.
     *   (clon.ne.'E' .and. clon.ne.'W' .and. clon.ne.' ')) go to 120

c
c     NEIC station file format
c
115   read(66,1234,err=150,end=1000) stat1,idla,imla,sla,clat,idlo,
     *                      imlo,slo,clon,elev,name

      if(stat1.eq.stat) then
c     print *,'neic ',stat1,idla,imla,sla,clat,idlo,imlo,slo,clon,
c    *         elev,name
         lat=dble(idla + ((dble(imla)+dble(sla)/60.d0)/60.d0))
         if(clat.eq.'S') lat=-lat
         lon=dble(idlo + ((dble(imlo)+dble(slo)/60.d0)/60.d0))
         if(clon.eq.'W') lon=-lon
	 if((idla.eq.0 .and. imla.eq.0 .and. sla.eq.0.) .and. 
     *	    (idlo.eq.0 .and. imlo.eq.0 .and. slo.eq.0.)  )then 
	    
	    if(name(1:28) .eq.'(Alternate Abbreviation for ') then
              read(name(29:33),'(a5)') statn
	      istan = trimle(statn)
	      if(statn(istan:istan).eq.')') istan = istan - 1
	      stat = statn(1:istan)
	      ialt = ialt + 1
	      if(ialt.le.2) then
	         rewind (66)
		 go to 115
	      endif
	    endif
	    ierr = 3
            go to 1000
	 endif
	 ierr = 0
         go to 1000
      endif
      go to 115

c
c     CSS 3.0 station file format
c
120   read(66,1235,err=150,end=1000) stat1,clat,lat,lon,elev,name
      if(stat1.eq.stat) then
c        print *,'css ',stat1,lat,lon,elev,name
	 elev = elev  * 1000.
	 ierr = 0
	 go to 1000
      endif
      go to 120

150   print *,' Cannot read station file (no NEIC or CSS 3.0 format)'
900   ierr = 1

1000  continue
      close(66)
      return
      end
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C
C
C     SUBPROGRAMM : DEPI
c
C         PROGRAMM COMPUTES :   - EPICENTRAL DISTANCES
C                               - AZIMUTHS AND BACKAZIMUTHS
C
C     From an old code of a main routine of unknwon source with several
C     changes.
C
C     changes October 1996 by J. Schweitzer, Bochum
C
C     here last changes July 24, 1997 by J. Schweitzer NORSAR
C        (fixing some numerical instabilities)
C
C
      subroutine depi(rlat,rlon,elat,elon,deld,delk,azid,bazd,d2km)
c
c
c     rlat, rlon = receiver geographical coordinates
c
c     elat, elon = event geographical coordinates
c     
c     deld       = epicentral distance in degree
c
c     delk       = epicentral distance in km
c
c     azid  =  epicenter (elat,elon) to station (rlat,rlon) !!!!!!
c    
c     bazd  =  backazimuth ( from station to event!!)
c
c     d2km  =  scaling factor degrees -> km
c
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)

      pi = 4.d0* datan(1.d0)
      pi2= pi/2.d0
      ep=rlat
      el=rlon
      stnp=elat
      stnl=elon
C
      DGSPR=180.d0/PI
      RAD=PI/180.d0
C
C     Check, if both coordinates are identical (here
C     assumed a distance less than ~ 1 meter)
C
      IF ( dabs(EP-STNP).lt. 1.d-6 .and.
     +     dabs(EL-STNL).lt. 1.d-6       ) then

        angi  = 0.d0
        range = 0.d0
        az    = 0.d0
        baz   = 0.d0
	d2km  = rad*radloc (EP)
	goto 1000

      endif
c
c     Now we need a 'mean' radius to convert distance in
c     [deg] on the sperical Earth into [km] with respect
c     to geographical latitude.
c

      drad = (ep-stnp)/100.d0

      srad = 0.d0
      jlat = 0

      do 50  radl = stnp,ep,drad

      srad = srad + radloc (radl)
      jlat = jlat + 1

50    continue

      d2km = rad * srad / dble(jlat)

      R90=90.d0*RAD
C
C **** MAKE ESQ1= 1.d0 FOR SPHERICAL EARTH
C      
c     esq1=1.d0
c     ESQ1=(1.d0-1.d0/298.257d0)**2.d0
c
c     EPIPR=90.d0*RAD-DATAN(ESQ1*dtan(EP*RAD))
c
      EPIPR=r90-convlat(EP,1)*rad
      EPIPC=EPIPR/RAD

C
C   if EL negative, EPILC=360+EL
C
      IF (EL.LT.0.d0) THEN
         EPILC=360.d0+EL
      ELSE
         EPILC=EL
      END IF

cj.s. EPILR=EPILC*RAD
C
C   save pol latitude
C
      E180=(R90-rad*convlat(-90.d0,1))/RAD
C
      if(dabs(stnp).eq.90.d0.and.stnl.ne.0.d0) stnl=0.d0

      STNPR=R90-rad*convlat(STNP,1)
      STPC=STNPR/RAD

C
C   if STNL negative, STLC=360+STNL
C
      IF (STNL.LT.0.d0) THEN
         STLC=360.d0+STNL
      ELSE
         STLC=STNL
      END IF

      IF (STPC.EQ.E180) THEN
         ANGI=E180-EPIPC
         AZ=180.d0
         BAZ=EL
         GOTO 998
      END IF
      if(stpc.eq.0.d0) then
         angi=epipc
         az=180.d0-el
         baz=0.d0
         go to 998
      endif
      if(epipc.eq.0.d0) then
         angi=stpc
         baz=0.d0
         az=180.d0-stnl
         go to 998
      endif
      if(epipc.eq.e180) then
         angi=e180-stpc
         az=stlc
         baz=180.d0
         go to 998
      endif

C
C   DETERMINE POLAR ANGLE
C
      PANG=ABS(STLC-EPILC)

      IF (PANG.GT.180.d0) PANG=360.d0-PANG
      PANG=PANG*RAD

      ANGI=DCOS(EPIPR)*DCOS(STNPR)+DSIN(EPIPR)*DSIN(STNPR)*DCOS(PANG)
      SNANG=DSQRT(DABS(1.d0-q2(ANGI)))
      if(ANGI.eq.0.d0) then
	angi = pi2
      else
        ANGI=DATAN(SNANG/ANGI)
      endif

C
C   ANGI negative?
C
      IF (ANGI.LT.0.d0) ANGI=ANGI+PI

      if(angi.ne.0.d0) then
         AZ=(DCOS(STNPR)-DCOS(EPIPR)*DCOS(ANGI))/(DSIN(EPIPR)
     *      *DSIN(ANGI))
      else
	 AZ= 0.d0
      endif

      SNAZ=DSQRT(DABS(1.d0-q2(AZ)))
      if (az.eq.0.d0) then
         az = pi2
      else
         AZ=DATAN(SNAZ/AZ)
      endif

C
C   AZ negative ?
C
      IF (AZ.LT.0.d0) AZ=AZ+PI
      AZ=DGSPR*AZ

      if(angi.ne.0.d0) then
         BAZ=(DCOS(EPIPR)-DCOS(STNPR)*DCOS(ANGI))/(DSIN(STNPR)
     *      *DSIN(ANGI))
      else
	 BAZ= 0.d0
      endif

      SNBAZ=DSQRT(DABS(1.d0-q2(BAZ)))
      if(baz.eq.0.d0) then
	 baz= pi2
      else
         BAZ=DATAN(SNBAZ/BAZ)
      endif

C
C   BAZ negative ?
C
      IF (BAZ.LT.0.d0) BAZ=BAZ+PI
      BAZ=BAZ*DGSPR
      ANGI=ANGI*DGSPR

C
C   ADJUST AZIMUTH AND BACKAZIMUTH
C
      IF (STLC-EPILC) 72,74,73
 72   IF (STLC-EPILC+180.d0) 80,77,81
 73   IF (STLC-EPILC-180.d0) 80,77,81
 74   IF (STPC-EPIPC) 75,78,76

 75   AZ=0.d0
      BAZ=180.d0
      GOTO 998

 76   AZ=180.d0
      BAZ=0.d0
      GOTO 998

 77   IF (STPC+EPIPC-E180) 78,78,79
 78   AZ=0.d0
      BAZ=0.d0
      GOTO 998

 79   AZ=180.d0
      BAZ=180.d0
      GOTO 998

 80   BAZ=360.d0-BAZ
      GOTO 998

 81   AZ=360.d0-AZ
 
C
C   CALCULATE DISTANCE IN KILOMETER
C
 998  RANGE=ANGI*d2km

1000  deld=angi
      delk=range
      bazd=az
      azid=baz
c
c
      return
      END
