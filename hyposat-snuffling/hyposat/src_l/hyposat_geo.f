	SUBROUTINE STEKIN (STLAT,STLONG,STNAME,STPOLY,KNAME,lui2,ierr)
C
C	to read the datafile with the SEISMOGEOGRAPHICAL REGIONS
C
C     input: polygon coordinates in degree ----> output: cartesian
C                                                coordin. in km
C               -------------------------------------------------
C	Format: Latitude      LAT          ----> STLAT(K)    
C		Longitude     LONG         ----> STLONG(K)    
C               plotcommand   ISTIFT ( not used, J.S.) 
C		abbrev. name  NAM          ----> STNAME     
C	        full name     KNAME        ----> KNAME
C   last line of a polygon: NAM = "**"
C		
C           number of polygonvertices            STPOLY
C
	CHARACTER*2  STNAME,NAM
	CHARACTER    KNAME*(*)
	INTEGER      STPOLY,ierr,istift,lui2
	REAL         STLAT(*),STLONG(*),LAT,LONG

C
C	INPUT: coordinates of the corners of the polygons 
C
	K = 0
	DO 5 I=1,50

	K = K + 1

	IF (I .EQ. 1) THEN
	  READ (LUI2,*,END=98) LAT,LONG,ISTIFT,STNAME,KNAME
	ELSE 
	  READ (LUI2,*,END=98) LAT,LONG,ISTIFT,NAM
	  IF (NAM .EQ. '**') GO TO 6          !last=first polygon corner
        ENDIF
C
	CALL GEOKOO (LAT,LONG,STY,STX)
C
	STLAT(k)=STY
	STLONG(k)=STX

5	CONTINUE

6	STPOLY = K-1                 !number of polygonvertices
        go to 99

