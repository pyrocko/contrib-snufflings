      SUBROUTINE HYPOSAT_GEO(GELAT, GELON, IGREG, ISREG, REGIONN, IERR)
c

      REAL*4       GELAT , GELON
      INTEGER*4    IGREG , ISREG, IERR
      CHARACTER*80 REGIONN*(*)

      CHARACTER*80 REGIO
      CHARACTER*80 GRNAME , SRNAME
      CHARACTER*1  DRCLAT , DRCLNG

      ierr = 0

      DRCLAT =' '
      DRCLNG =' '

      REGIO   = ' '

c
c     If possible, we try to get the name of the seismotectonic 
c     unit.
c     (Level III regionalisation)
c
      CALL STEC_UNIT(GELAT,GELON,REGIO)

      CALL LOOKUP(GELAT,DRCLAT,GELON,DRCLNG,IGREG,ISREG)
      CALL REGION(IGREG,GRNAME,ISREG,SRNAME)

      IF(REGIO.NE.'OUT-OF-RANGE') then
	 REGIONN = REGIO
      else
	 REGIONN = GRNAME
      endif
 
      RETURN
      END


