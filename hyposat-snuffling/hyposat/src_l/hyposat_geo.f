98	IERR = 999                   ! end of file reached
C
C
99	RETURN
	END
************************************************************************
	SUBROUTINE GEOKOO (LAT,LONG,YLAT,XLONG)
C	for input of LAT and LONG in degree
C
C	OUTPUT: YLAT = y-Coordinate in km
C	        XLONG = x-Coordinate in km
C
C      TRANSFORMATION of geographical coordinates in cartesian  
C      coordinates after C.F.RICHTER: ELEMENTARY SEISMOLOGY. pp 701-704.
C      W.H.FREEMAN and CO. SAN FRANCISCO and LONDON. 1958.
C
	REAL LAT,LONG
	DATA PPP/1.859061/,QQ/1.853842/
C       the values for PPP and QQ are for 50 degree latitude
	DATA LORED/10.0/,LARED/50.0/
C	the computation of the cartesian coordinates is related to
C		50 degree N  and  10 degree E 
C	as origin of the new coordinate system
C
        ZLAT=LAT/57.29578
C	PP and QQ : km/minute(angel)
	PP=PPP*COS(ZLAT)                    
	DX=(LONG-LORED)*60.*PP                       !cartesian X-value
	DY=(LAT-LARED)*60.*QQ                        !cartesian Y-value
	IF(DX*DX .LT. 0.000001)DX=0.001
	IF(DY*DY .LT. 0.000001)DY=0.001
	YLAT=DY
	XLONG=DX
C
	RETURN
	END
C***********************************************************************
	FUNCTION INSIDE (X0,Y0,PX,PY,N)
C	GODKIN,C.B. AND J.J.PULLI: APPLICATION OF THE "WINDING-NUMBER
C	ALGORITHM" TO THE SPATIAL SORTING OF CATALOGED EARTHQUAKE DATA.
C	BSSA 75, 5, PP. 1845-1848, OCTOBER 1984
C
C	CHECK IF POINT X0,Y0 LIES INSIDE POLYGONE
C
C	PARAMETERS:
C	X0,Y0    POINT TO TEST
C	PX       ARRAY OF X-COORDINATES OF POLYGON
C	PY       ARRAY OF Y-COORDINATES OF POLYGON
C       N        NUMBER OF ELEMENTS OF PX AND PY OR OF SIDES OF POLYGON
C
C	RETURN VALUE:    0  IF POINT OUTSIDE
C                     +/-1  IF POINT INSIDE 
C                        2  IF POINT IS ON AN EDGE OR VERTEX
C
	REAL PX(50),PY(50)
C
C	ACCUMULATE SIGNED CROSSING NUMBERS WITH INSIDE
	INSIDE=0
	DO 200 I=1,N-1
C	PROCEED AROUND POLYGON CHECKING EACH SEGMENT TO SEE IF 
C	NEGATIVE X-AXIS WAS CROSSED
C       TRANSLATE COORDINATES OF POLYGON TO PUT TEST POINT AT ORIGIN
	ISICR=KSICR(PX(I)-X0,PY(I)-Y0,PX(I+1)-X0,PY(I+1)-Y0)
C	type *, I,'PX=',PX(I),'PY=',PY(I),' X0=',X0,' Y0=',Y0
C	STOP COUNTING IF POINT IS ON EDGE
	IF(ISICR .EQ. 4) GO TO 600
	INSIDE=INSIDE+ISICR
200	CONTINUE
C
C       CHECK SEGMENT FROM LAST VERTEX TO FIRST VERTEX
	ISICR=KSICR(PX(N)-X0,PY(N)-Y0,PX(1)-X0,PY(1)-Y0)
	IF(ISICR .EQ. 4) GO TO 600
	INSIDE=(INSIDE+ISICR)/2
C	type *, ' N= ',N,' INSIDE=',INSIDE,' ISICR=',ISICR
C
	RETURN
C
600     INSIDE=2
C
C	type *, ' N= ',N,' INSIDE=',INSIDE,' ISICR=',ISICR
	RETURN
	END
C**********************************************************************
	FUNCTION KSICR(X1,Y1,X2,Y2)
C	COMPUTE SIGNED CROSSING NUMBER
C
C	A "SIGNED CROSSING NUMBER" TELLS WETHER A SEGMENT 
C	(IN THIS CASE THE SEGMENT FROM (X1,Y1) TO (X2,Y2))
C	CROSSES THE NEGATIVE X-AXIS OR GOES THROUGH THE ORIGIN
C
C	THE RETURN VALUES ARE:
C             +2 IF SEGMENT CROSSES FROM BELOW
C	      +1 IF SEGMENT EITHER ENDS ON -X-AXIS FROM BELOW OR STARTS
C                           UPWARDS FROM -X-AXIS ("HALF CROSSING")
C	       0 IF THERE IS NO CROSSING
C	      -1 IF SEGMENT EITHER ENDS ON -X-AXIS FROM ABOVE OR STARTS
C                           DOWNWARDS FROM -X-AXIS ("HALF CROSSING")
C             -2 IF SEGMENT CROSSES FROM ABOVE
C             +4 IF SEGMENT CROSSES THROUGH THE ORIGIN
C
C
C	IF BOTH POINTS ARE ON THE SAME SIDE OF X-AXIS, RETURN 0
	IF (Y1*Y2 .GT. 0) GO TO 600
C
C       CHECK IF SEGMENT CROSSES THROUGH THE ORIGIN
        IF (X1*Y2 .NE. X2*Y1 .OR. X1*X2 .GT. 0) GO TO 100
	KSICR=4
	RETURN
C       
C	CHECK FOR COMPLETE CROSSING
100     IF(Y1*Y2 .LT. 0) GO TO 300
C
C       HALF CROSSING?
C       ONE END OF SEGMENT TOUCHES X-AXIS! WHICH END?       
        IF (Y2 .EQ. 0) GO TO 200
C       HERE Y1=0   CHECK IF SEGMENT TOUCHES +X-AXIS
        IF (X1 .GT. 0) GO TO 600
C       UPWARD OR DOWNWARD?
        IF (Y2 .GT. 0) GO TO 700
	GO TO 800
C
C       HERE Y2=0   CHECK IF SEGMENT TOUCHES +X-AXIS
200     IF (Y1 .EQ. 0 .OR. X2 .GT. 0) GO TO 600
C       UPWARD OR DOWNWARD?
        IF (Y1 .GT. 0) GO TO 800
	GO TO 700
C
C       COMPLETE CROSSING OF -X-AXIS?
C       BREAK INTO CASES ACCORDING TO CROSSING DIRECTION
300	IF (Y1 .GT. 0) GO TO 400
C
C       CASE   Y1 < 0 < Y2
	IF (X1*Y2 .GE. Y1*X2) GO TO 600
C	UPWARD CROSSING
	KSICR=2
 	RETURN
C
C       CASE   Y1 > 0 > Y2
400	IF (Y1*X2 .GE. X1*Y2) GO TO 600
C	DOWNWARD CROSSING
	KSICR=-2
 	RETURN
C
C	NO CROSSING
600	KSICR=0
 	RETURN
C
C	UPWARD HALF CROSSING
700	KSICR=1
 	RETURN
C
C	DOWNWARD HALF CROSSING
800	KSICR=-1
 	RETURN
	END
C
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C
C     This function DIRDEL (DIRect DELta) calculates the epicentral 
c     distance for an upgoing phase at a source depth greater than 0.
c
c     We use the AK135 as standard velocity model and assume that the
c     distance differences for such phases in different models is
c     relatively small.
c
c     Model AK135 is here hard coded via DATA statements.
c
c     input:
c     
c               p      -  ray paramter of upgoing phase
c
c               zo     -  source depth
c
c               fa     -  factor how often this phase passes the
c                         structure (here usually fa = 1.d0)
c
c               type   -  character, gives phase type (P or S)
c
c     output 
c               
c               as funtion : distance in degrees
c
c     programmer:   J.Schweitzer, NORSAR, March 1999
c
cccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccc

      function dirdel(p0,zo,fa,type)

      IMPLICIT DOUBLE PRECISION (A-H,O-Z)

      PARAMETER (nz=24)

      character   type*1

      dimension v0(2,nz), v(nz), v2(nz), g(nz), z(nz), z0(nz), h(nz)

      data z0/  0. , 20. , 20. , 35. , 35. , 77.5,120. ,165. ,210. ,
     +        210. ,260. ,310. ,360. ,410. ,410. ,460. ,510. ,560. ,
     +        610. ,660. ,660. ,710. ,760. ,809.5/
      data v0/ 5.8   , 5.8   , 6.5   , 6.5   , 8.04  , 8.045 , 8.05  ,
     +         8.175 , 8.3   , 8.3   , 8.4825, 8.665 , 8.8475, 9.03  , 
     +         9.36  , 9.528 , 9.696 , 9.864 ,10.032 ,10.2   ,10.79  ,
     +        10.9229,11.0558,11.1353, 
     +         3.46  , 3.46  , 3.85  , 3.85  , 4.48  , 4.49  , 4.5   ,
     +         4.509 , 4.518 , 4.523 , 4.609 , 4.696 , 4.783 , 4.87  ,
     +         5.08  , 5.186 , 5.292 , 5.398 , 5.504 , 5.61  , 5.96  , 
     +         6.0897, 6.2095, 6.2426/

      PI  = 4.d0*DATAN(1.d0)
      PIM = PI/180.d0
      RE  = 6371.d0
      AA  = PIM*RE

      p = dabs(p0)

      dirdel = 0.0d0

      if(type.eq.'P') iph = 1
      if(type.eq.'S') iph = 2

      vmax = 0.d0

      DO 500 I=1,nz

      i1 = i + 1

      aiz  = re - Z0(i)
      V(i) = V0(iph,i)*re/aiz
      Z(i) = re*dlog(re/aiz)
      if(vmax.lt.v(i)) vmax = v(i)

      if(zo.ge.z0(i1)) then
         if(zo.eq.z0(i)) then
	    aiz   = re - Z0(i1)
            V(i1) = V0(iph,i1)*re/aiz
            Z(i1) = re*dlog(re/aiz)
            nl    = i1
            if(vmax.lt.v(i1)) vmax = v(i1)
            go to 550
         endif
      else

         dz = (zo-Z0(i))/(Z0(i1)-Z0(i))
	 aiz   = re - zo
         V(i1) = (v0(iph,i) + dz * (V0(iph,i1)-v0(iph,i)))*re/aiz
	 Z(i1) = re*dlog(re/aiz)
         if(vmax.lt.v(i1)) vmax = v(i1)
         nl    = i1
         go to 550

      endif

500   continue

550   do 600 i = 1,nl

      I2=I+1
      H(I)=Z(I2)-Z(I)
      IF(H(I).EQ.0.d0)  H(I)=0.000001d0
      V2(I)=V(I2)
      IF(V2(I).EQ.V(I)) THEN
         V2(I)=1.000001d0*V(I)
         V(I2)=V2(I)
      ENDIF
600   G(I)=(V2(I)-V(I))/H(I)
 
      RVV = p / ((re-zo)*PIM)
      if(rvv*vmax.gt.1.d0) rvv=1.d0/vmax

      R=0.D0

      DO  1100  KK=1,NL-1
      E=V(KK)
      G1=E*RVV
      P=DSQRT(DABS(1.D0-G1*G1))
      O=1.d0/G(KK)
      F=V2(KK)
      G1=F*RVV
      Q=DSQRT(DABS(1.D0-G1*G1))
      R=R+FA*(P-Q)*O
1100  CONTINUE

      dirdel = r/(aa*rvv)

      return
      end
cccccccccccccccccccccccccccccccccccccccccccccccccccccccc
c
c     function radloc
c
c     calculates the 'local' Earth radius using the
c     Earth's geometry and the local (geocentric) latitude.
c
      function radloc(dlat)
      double precision dlat,radloc,rada,radb,radian,q2

c
c     Earth geometry after Stacey (1992) 'Physics of the Earth'
c
      rada = 6378.136d0
      radb = 6356.751d0

      radian = datan(1.d0) / 45.d0

      radloc = dsqrt( q2( rada*dcos(radian*dlat) ) +
     +                q2( radb*dsin(radian*dlat) )   )

      return
      end
